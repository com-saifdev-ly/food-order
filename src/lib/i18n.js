export function saveLanguagePreference(language) {
  try {
    localStorage.setItem('preferredLanguage', language);
  } catch (error) {
    console.error('Failed to save language preference:', error);
  }
}

export function getLanguagePreference() {
  try {
    return localStorage.getItem('preferredLanguage');
  } catch (error) {
    console.error('Failed to get language preference:', error);
    return null;
  }
}

export function getLanguage(location = window.location, browserNavigator = navigator) {
  const params = new URLSearchParams(location.search);
  const requestedLanguage = params.get('lang');

  if (requestedLanguage === 'ar' || requestedLanguage === 'en') {
    saveLanguagePreference(requestedLanguage);
    return requestedLanguage;
  }

  // Check localStorage first
  const savedLanguage = getLanguagePreference();
  if (savedLanguage === 'ar' || savedLanguage === 'en') {
    return savedLanguage;
  }

  return getPreferredLanguage(browserNavigator);
}

export function getLocalizedPath(pathname, language) {
  const url = new URL(pathname, window.location.origin);
  url.searchParams.set('lang', language);
  return `${url.pathname}${url.search}`;
}

export function getLanguageLink(nextLanguage, location = window.location) {
  const url = new URL(location.href);
  url.searchParams.set('lang', nextLanguage);
  return `${url.pathname}${url.search}${url.hash}`;
}